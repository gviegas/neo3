func TestPipeline(t *testing.T) {
	// Descriptors.
	dtex := driver.Descriptor{
		Type:   driver.DTexture,
		Stages: driver.SFragment,
		Nr:     1,
		Len:    1,
	}
	dsplr := driver.Descriptor{
		Type:   driver.DSampler,
		Stages: driver.SFragment,
		Nr:     2,
		Len:    1,
	}
	heap, err := tDrv.NewDescHeap([]driver.Descriptor{dtex, dsplr})
	if err != nil {
		t.Fatal("NewDescHeap failed, cannot test NewPipeline")
		return
	}
	defer heap.Destroy()
	desc, err := tDrv.NewDescTable([]driver.DescHeap{heap})
	if err != nil {
		t.Fatal("NewDescTable failed, cannot test NewPipeline")
		return
	}
	defer desc.Destroy()
	dconst := driver.Descriptor{
		Type:   driver.DConstant,
		Stages: driver.SVertex,
		Nr:     0,
		Len:    1,
	}
	heap2, err := tDrv.NewDescHeap([]driver.Descriptor{dconst})
	if err != nil {
		t.Fatal("NewDescHeap failed, cannot test NewPipeline")
		return
	}
	defer heap2.Destroy()
	desc2, err := tDrv.NewDescTable([]driver.DescHeap{heap2})
	if err != nil {
		t.Fatal("NewDescHeap failed, cannot test NewPipeline")
		return
	}
	defer desc2.Destroy()
	dbuf := driver.Descriptor{
		Type:   driver.DBuffer,
		Stages: driver.SCompute,
		Nr:     0,
		Len:    1,
	}
	heap3, err := tDrv.NewDescHeap([]driver.Descriptor{dbuf})
	if err != nil {
		t.Fatal("NewDescHeap failed, cannot test NewPipeline")
		return
	}
	defer heap3.Destroy()
	desc3, err := tDrv.NewDescTable([]driver.DescHeap{heap3})
	if err != nil {
		t.Fatal("NewDescTable failed, cannot test NewPipeline")
		return
	}
	defer desc3.Destroy()

	// Shaders.
	vert, err := tDrv.NewShaderCode(tPlVert[:])
	if err != nil {
		t.Fatal("NewShaderCode failed, cannot test NewPipeline")
		return
	}
	defer vert.Destroy()
	frag, err := tDrv.NewShaderCode(tPlFrag[:])
	if err != nil {
		t.Fatal("NewShaderCode failed, cannot test NewPipeline")
		return
	}
	defer frag.Destroy()
	vert2, err := tDrv.NewShaderCode(tPlVert2[:])
	if err != nil {
		t.Fatal("NewShaderCode failed, cannot test NewPipeline")
		return
	}
	defer vert2.Destroy()
	frag2, err := tDrv.NewShaderCode(tPlFrag2[:])
	if err != nil {
		t.Fatal("NewShaderCode failed, cannot test NewPipeline")
		return
	}
	defer frag2.Destroy()
	vert3, err := tDrv.NewShaderCode(tPlVert3[:])
	if err != nil {
		t.Fatal("NewShaderCode failed, cannot test NewPipeline")
		return
	}
	defer vert3.Destroy()
	comp, err := tDrv.NewShaderCode(tPlComp[:])
	if err != nil {
		t.Fatal("NewShaderCode failed, cannot test NewPipeline")
		return
	}
	defer comp.Destroy()

	// Pipeline.
	pl := [4]struct {
		pl  driver.Pipeline
		err error
	}{}
	pl[0].pl, pl[0].err = plTestNew(vert, frag, desc, driver.RGBA8un, driver.D32fS8ui)
	pl[1].pl, pl[1].err = plTestNewColorOnly(vert2, frag2, desc2, driver.BGRA8sRGB)
	pl[2].pl, pl[2].err = plTestNewDepthOnly(vert3, driver.D16un)
	pl[3].pl, pl[3].err = plTestNewComp(comp, desc3)
	zp := pipeline{}
	for i, p := range pl {
		call := fmt.Sprintf("tDrv.NewPipeline(state) [%d]", i)
		if p.err != nil {
			if p.pl != nil {
				t.Fatalf("%s\nhave %p, %v\nwant nil, %v", call, p.pl, p.err, p.err)
			} else {
				t.Fatalf("(error) %s: %v", call, p.err)
			}
		} else if p.pl == nil {
			t.Fatalf("%s\nhave nil, nil\nwant non-nil, nil", call)
		} else {
			p := p.pl.(*pipeline)
			if p.d != &tDrv {
				t.Fatalf("%s: p.d\nhave %p\nwant %p", call, p.d, &tDrv)
			}
			if p.pl == zp.pl {
				t.Fatalf("%s: p.pl\nhave %v\nwant valid handle", call, p.pl)
			}
			p.Destroy()
			if *p != zp {
				t.Fatalf("p.Destroy(): p\nhave %v\nwant %v", p, zp)
			}
		}
	}
}

