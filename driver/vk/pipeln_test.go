// plTestNew creates a graphics pipeline for testing.
func plTestNew(vert, frag driver.ShaderCode, desc driver.DescTable, color driver.PixelFmt, ds driver.PixelFmt) (driver.Pipeline, error) {
	gs := driver.GraphState{
		VertFunc: driver.ShaderFunc{
			Code: vert,
			Name: "main",
		},
		FragFunc: driver.ShaderFunc{
			Code: frag,
			Name: "main",
		},
		Desc: desc,
		Input: []driver.VertexIn{
			{
				Format: driver.Float32x3,
				Stride: 4 * 3,
				Nr:     0,
			},
			{
				Format: driver.Float32x2,
				Stride: 4 * 2,
				Nr:     3,
			},
		},
		Topology: driver.TTriangle,
		Raster: driver.RasterState{
			Discard:   false,
			Clockwise: false,
			Cull:      driver.CBack,
			Fill:      driver.FFill,
			DepthBias: false,
		},
		Samples: 1,
		DS: driver.DSState{
			DepthTest:   true,
			DepthWrite:  true,
			DepthCmp:    driver.CLess,
			StencilTest: true,
			Front: driver.StencilT{
				FailS:     driver.SKeep,
				FailD:     driver.SKeep,
				Pass:      driver.SReplace,
				ReadMask:  0,
				WriteMask: 255,
				Cmp:       driver.CAlways,
			},
			Back: driver.StencilT{
				FailS:     driver.SKeep,
				FailD:     driver.SKeep,
				Pass:      driver.SReplace,
				ReadMask:  0,
				WriteMask: 255,
				Cmp:       driver.CAlways,
			},
		},
		Blend: driver.BlendState{
			IndependentBlend: false,
			Color: []driver.ColorBlend{
				{
					Blend:     true,
					WriteMask: driver.CAll,
					SrcFacRGB: driver.BSrcAlpha,
					DstFacRGB: driver.BInvSrcAlpha,
					OpRGB:     driver.BAdd,
					SrcFacA:   driver.BOne,
					DstFacA:   driver.BZero,
					OpA:       driver.BAdd,
				},
			},
		},
		ColorFmt: []driver.PixelFmt{color},
		DSFmt:    ds,
	}
	return tDrv.NewPipeline(&gs)
}

